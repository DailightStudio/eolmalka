export type Locale = "ko" | "en" | "ja";

function detectLocale(): Locale {
  try {
    const l = new Intl.DateTimeFormat().resolvedOptions().locale ?? "ko";
    if (l.startsWith("en")) return "en";
    if (l.startsWith("ja")) return "ja";
  } catch {
    // Hermes Intl 없으면 폴백
  }
  return "ko";
}

